$('#profile-icon').css({'color':'#374963'})
$('#profile-icon').addClass('active')
$('#Home').click(function(){
    $('#profile-icon').css({'color':'#6a6e72'})
    $('#settings').css({'color':'#6a6e72'})
    $('#Noti').css({'color':'#6a6e72'})
    $('#Noti').removeClass('active')
    $('#settings').removeClass('active')
    $('#profile-icon').removeClass('active')
    $(this).css({'color':'#d9ddff'})
    $(this).addClass('active')
})
